public class Prof {
    String id;
    String name;

    public Prof(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public Prof(){}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



}
